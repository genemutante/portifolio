// --- VARIÁVEIS DE CONFIGURAÇÃO DO DEBUG MODE ---
    const DEBUG_CODE = "solipe"; 
    const DEBUG_STATUS_KEY = "isDebugActive"; // Chave no localStorage para persistir o estado

    function applyLanguage(lang) {
      // 1. Capta o estado do Debug Mode (salvo no localStorage)
      const isDebugActive = localStorage.getItem(DEBUG_STATUS_KEY) === 'true';
      const debugMode = isDebugActive; // Agora o estado depende do localStorage/verificação

      const elements = document.querySelectorAll("[data-i18n]");
      const dict = translations[lang] || translations["pt"];

      // Atualiza o visual do controle do Debug Mode no cabeçalho (HTML precisa ter #debugModeLabel e #activateDebugBtn)
      const label = document.getElementById('debugModeLabel');
      const button = document.getElementById('activateDebugBtn');
      if (label && button) {
          if (debugMode) {
              label.textContent = "";
              button.textContent = "Desativar DM";
              button.style.backgroundColor = 'rgba(0, 255, 0, 0.2)'; // Fundo verde claro para ativo
          } else {
              label.textContent = "";
              button.textContent = "DM";
              button.style.backgroundColor = 'transparent';
          }
      }

      elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        let text = dict[key]; 
        
        if (text) {
          // 2. Anexa a chave se o Debug Mode estiver ligado (AGORA LENDO isDebugActive)
          if (debugMode) {
            text = text + ` (${key})`; 
          }
          
          el.innerHTML = text;
        }
      });

      localStorage.setItem("seqLang", lang);
    }

    document.addEventListener("DOMContentLoaded", () => {
      const langSelect = document.getElementById("langSelect");
      const savedLang = localStorage.getItem("seqLang") || "pt";
      const currentLang = savedLang; // Usaremos 'currentLang' para as chamadas futuras

      // NOVO: Listener para o Botão de Ativação/Desativação do Debug Mode
      const activateDebugBtn = document.getElementById('activateDebugBtn');

      if (activateDebugBtn) {
          activateDebugBtn.addEventListener('click', function() {
              const isActive = localStorage.getItem(DEBUG_STATUS_KEY) === 'true';

              if (isActive) {
                  // DESATIVA: Limpa o estado e re-traduz
                  localStorage.setItem(DEBUG_STATUS_KEY, 'false');
                  alert("Debug Mode desativado.");
                  applyLanguage(currentLang);
              } else {
                  // ATIVA: Solicita o código
                  const inputCode = prompt("Informe o código de ativação do Debug Mode:");

                  if (inputCode === DEBUG_CODE) {
                      // Código correto: Salva o estado e re-traduz
                      localStorage.setItem(DEBUG_STATUS_KEY, 'true');
                      alert("Acesso concedido. Debug Mode ativado.");
                      applyLanguage(currentLang);
                  } else if (inputCode !== null && inputCode !== '') {
                      // Código incorreto (e não cancelado/vazio)
                      alert("Acesso negado. Código incorreto.");
                  }
                  // Se o usuário cancelou (inputCode é null) ou deixou vazio, não faz nada.
              }
          });
      }


      // Lógica de Carregamento de Idioma Existente
      if (langSelect) {
        langSelect.value = currentLang;
        // A chamada inicial agora aplica o debug se estiver salvo no localStorage
        applyLanguage(currentLang); 

        langSelect.addEventListener("change", () => {
          applyLanguage(langSelect.value);
        });
      } else {
        applyLanguage(currentLang); 
      }
      
      // O listener do checkbox de debug anterior é REMOVIDO, pois o botão agora gerencia o estado.
      
    });





